-- 9. Calculate yearly revenue growth percentage.
with yearly_revenue as (
	select extract(year from sale_date) sales_year,
			sum(revenue_usd) as total_revenue
	from energy_sales group by extract(year from sale_date)
)
select sales_year,total_revenue ,
	lag(total_revenue) over(order by sales_year) as previeus_revenue,
   round( total_revenue-lag(total_revenue) over(order by sales_year) /
    lag(total_revenue) over(order by sales_year) *100,2)
    as revenue_growth

from  yearly_revenue
order by sales_year;

    
-- 10. Find plants having more than 3 grid failures in a month.
SELECT 
    p.plant_id,
    p.plant_name,
    EXTRACT(YEAR FROM f.failure_date) AS failure_year,
    EXTRACT(MONTH FROM f.failure_date) AS failure_month,
    COUNT(f.failure_id) AS total_failures
FROM grid_failures f INNER JOIN  plant_master p ON f.plant_id = p.plant_id
GROUP BY  p.plant_id,
    p.plant_name,
    EXTRACT(YEAR FROM f.failure_date),
    EXTRACT(MONTH FROM f.failure_date)
HAVING COUNT(f.failure_id) > 3
ORDER BY 
    failure_year DESC, 
    failure_month DESC, 
    total_failures DESC;
    

